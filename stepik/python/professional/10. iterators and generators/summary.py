import itertools # импорт модуля для работы с итераторами
from itertools import * # импорт всех функций
'----------------------'
# МОДУЛЬ ITERTOOLS:
# Функции для создания эффективных итераторов
# Лучшие характеристики памяти чем списки
# Функции возвращают итераторы (ленивые вычисления)
'----------------------'
# ФУНКЦИИ, ПОРОЖДАЮЩИЕ ДАННЫЕ (бесконечные итераторы):
count(start=0, step=1)
# бесконечная последовательность чисел с заданным шагом

cycle(iterable)
# циклично повторяет элементы итерируемого объекта

repeat(object, times=None)
# повторяет один объект заданное количество раз (или бесконечно)
'----------------------'
# ФУНКЦИИ, ФИЛЬТРУЮЩИЕ ДАННЫЕ:
dropwhile(predicate, iterable)
# пропускает элементы пока условие истинно, затем выдает все остальные

takewhile(predicate, iterable)
# выдает элементы пока условие истинно, затем останавливается

filterfalse(predicate, iterable)
# выдает элементы для которых условие ложно (противоположность filter)

compress(iterable, selectors)
# выдает элементы где соответствующий selector равен True

islice(iterable, start, stop, step)
# срез итератора (аналог list[start:stop:step])
'----------------------'
# ФУНКЦИИ, ОБЪЕДИНЯЮЩИЕ И РАЗДЕЛЯЮЩИЕ:
chain(*iterables)
# последовательно соединяет итерируемые объекты

chain.from_iterable(iterable)
# соединяет вложенные итерируемые объекты

zip_longest(*iterables, fillvalue=None)
# как zip(), но до исчерпания самого длинного объекта

tee(iterable, n=2)
# создает n независимых итераторов из одного

pairwise(iterable)
# последовательные перекрывающиеся пары (Python 3.10+)
'----------------------'
# ФУНКЦИИ, ГРУППИРУЮЩИЕ ДАННЫЕ:
groupby(iterable, key=None)
# группирует смежные одинаковые элементы
# возвращает (ключ, итератор_группы)
'----------------------'
# КОМБИНАТОРНЫЕ ФУНКЦИИ:
permutations(iterable, r=None)
# все перестановки длины r (размещения)

combinations(iterable, r)
# все сочетания длины r без повторений

combinations_with_replacement(iterable, r)
# все сочетания длины r с повторениями

product(*iterables, repeat=1)
# декартово произведение итерируемых объектов
'----------------------'
# ДОПОЛНИТЕЛЬНЫЕ ФУНКЦИИ:
starmap(function, iterable)
# применяет функцию к элементам как map(*args)

accumulate(iterable, func=operator.add, initial=None)
# накопленные результаты применения функции
'----------------------'
# ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ:
# Бесконечная последовательность:
for i in count(10, 2):  # 10, 12, 14, 16...
    if i > 20: break

# Циклическое повторение:
colors = cycle(['red', 'green', 'blue'])
next(colors)  # 'red', затем 'green', затем 'blue', затем снова 'red'

# Фильтрация до первого ложного:
takewhile(lambda x: x < 5, [1, 2, 3, 6, 4])  # [1, 2, 3]

# Группировка смежных элементов:
for key, group in groupby('AAABBCC'):
    print(f'{key}: {list(group)}')  # A: [A,A,A], B: [B,B], C: [C,C]

# Все сочетания:
list(combinations('ABC', 2))  # [('A','B'), ('A','C'), ('B','C')]

# Декартово произведение:
list(product('AB', '12'))  # [('A','1'), ('A','2'), ('B','1'), ('B','2')]
'----------------------'
# ОСОБЕННОСТИ:
# ✔️ Все функции возвращают итераторы (ленивые)
# ✔️ Бесконечные итераторы: count(), cycle(), repeat()
# ✔️ groupby() работает только со смежными элементами
# ✔️ Для полной группировки нужна предварительная сортировка
# ✔️ tee() создает независимые копии итератора
# ❌ Комбинаторные функции могут использовать много памяти
# ❌ Нельзя создать список из бесконечного итератора
# ❌ groupby() итераторы групп становятся пустыми при переходе к следующей
'----------------------'
# ЧАСТЫЕ КОМБИНАЦИИ:
# Удаление дубликатов смежных элементов:
[key for key, group in groupby(data)]

# Замена вложенных циклов:
product(range(3), repeat=2)  # вместо for i in range(3): for j in range(3)

# Объединение списков:
list(chain.from_iterable([[1,2], [3,4], [5,6]]))  # [1,2,3,4,5,6]
