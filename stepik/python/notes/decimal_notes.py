from decimal_notes import * # импорт модуля для точных десятичных вычислений
'----------------------'
# ФОРМАТ DECIMAL:
# Десятичные числа с основанием экспоненты 10
# Точные вычисления (в отличие от float)
# Программная реализация (медленнее float)
# Настраиваемая точность (по умолчанию 28 знаков)
'----------------------'
# СОЗДАНИЕ DECIMAL ЧИСЕЛ:
Decimal(value)
# value может быть int, str, но НЕ float

Decimal(1)          # из целого числа
Decimal('3.14')     # из строки (рекомендуется)
Decimal('0.1')      # НЕ Decimal(0.1) - избегайте float!
'----------------------'
# АРИФМЕТИЧЕСКИЕ ОПЕРАЦИИ:
# Поддерживаются все стандартные операции: +, -, *, /, //, **, %
# Можно смешивать с int, но НЕ рекомендуется с float

num1 = Decimal('5.2')
num2 = Decimal('2.3')
result = num1 + num2    # результат тоже Decimal
'----------------------'
# МАТЕМАТИЧЕСКИЕ МЕТОДЫ:
num.sqrt()              # квадратный корень
num.exp()               # e^x
num.ln()                # натуральный логарифм (ln)
num.log10()             # десятичный логарифм (log10)
'----------------------'
# МЕТОД as_tuple():
num.as_tuple()
# возвращает DecimalTuple(sign, digits, exponent)
# sign: 0 для положительных, 1 для отрицательных
# digits: кортеж цифр числа
# exponent: степень 10 (отрицательная для дробных)
'----------------------'
# ОКРУГЛЕНИЕ:
num.quantize(Decimal('1.00'), rounding=ROUND_HALF_EVEN)
# округление до формата указанного в первом аргументе

# Форматы округления:
Decimal('1.000')    # до 3 знаков после запятой
Decimal('1.00')     # до 2 знаков после запятой
Decimal('1.0')      # до 1 знака после запятой
Decimal('1')        # до целого числа
'----------------------'
# СТРАТЕГИИ ОКРУГЛЕНИЯ:
ROUND_CEILING       # к плюс бесконечности
ROUND_FLOOR         # к минус бесконечности
ROUND_DOWN          # к нулю
ROUND_UP            # от нуля
ROUND_HALF_DOWN     # к ближайшему, при равенстве к нулю
ROUND_HALF_EVEN     # к ближайшему четному (по умолчанию)
ROUND_HALF_UP       # к ближайшему, при равенстве от нуля
ROUND_05UP          # от нуля если последняя цифра 0 или 5
'----------------------'
# РАБОТА С КОНТЕКСТОМ:
getcontext()
# получение текущего контекста

getcontext().prec = 3
# установка точности (количество значащих цифр)

getcontext().rounding = ROUND_HALF_UP
# установка стратегии округления
'----------------------'
# СРАВНЕНИЯ:
# Decimal числа можно сравнивать точно:
Decimal('0.1') * 3 == Decimal('0.3')  # True
0.1 * 3 == 0.3                        # False

# Decimal('0.1') + Decimal('0.1') + Decimal('0.1') == Decimal('0.3')  # True
'----------------------'
# ВЗАИМОДЕЙСТВИЕ С МОДУЛЕМ math:
from math import sqrt, sin

# math функции принимают Decimal, но возвращают float
result = sqrt(Decimal('2.0'))  # result имеет тип float
'----------------------'
# ОСОБЕННОСТИ:
# ✔️ Создавайте из строк, не из float: Decimal('0.1')
# ✔️ Точность влияет только на операции, не на создание
# ✔️ Можно смешивать с int, избегайте смешивания с float
# ✔️ Поддерживается точное сравнение на равенство
# ✔️ Настраиваемая точность и округление
# ❌ Медленнее float в разы
# ❌ Занимает больше памяти
# ❌ quantize() требует достаточной точности контекста
'----------------------'
