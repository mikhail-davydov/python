def tribonacci(n: int) -> list:
    tri_list = []
    f1, f2, f3 = 1, 1, 1
    for i in range(n):
        tri_list.append(f1)
        f1, f2, f3 = f2, f3, f1 + f2 + f3
    return tri_list


